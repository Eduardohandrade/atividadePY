#Faça um algoritmo para calcular quantas ferraduras são necessárias para equipar todos os cavalos comprados para um haras.
num_cavalos = int(input("Digite o número de cavalos do haras: "))

num_ferraduras = num_cavalos * 4

print("Serão necessárias", num_ferraduras, "ferraduras para equipar todos os cavalos do haras.")